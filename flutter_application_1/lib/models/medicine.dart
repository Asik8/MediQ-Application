class Medicine {
  final String id;
  final String name;
  final String brand;
  final double price;
  final String dosage;
  final String quantity;
  final String imageUrl;
  final String description;

  Medicine({
    required this.id,
    required this.name,
    required this.brand,
    required this.price,
    required this.dosage,
    required this.quantity,
    required this.imageUrl,
    required this.description,
  });
}